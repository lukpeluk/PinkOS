// funca con caracteres imprimibles soportados por la tipografÃ­a, y con el salto de lÃ­nea
// hace wrapping automÃ¡tico, podrÃ­a configurarse con un flag
// tambiÃ©n permite borrar un caracter (enviando el PinkMapping correspondiente)
// para borrarlo, hay que primero configurar x e y con las coordenadas del caracter a borrar
// se encarga de no incrementar x e y ya que el siguiente char debe ir en la misma posiciÃ³n
// para borrar le escribe un espacio en blanco por arriba
void drawChar(char c, uint32_t textColor, uint32_t bgColor) {
    int is_deleting = 0;

	// salto de lÃ­nea (antes del wrapping pues este no le afecta)
	if(c == 37){
        x = 0;
		y += CHAR_HEIGHT + INTERLINE;
		return;
	}

    // no tiene sentido un wrapping vertical, asÃ­ que si se pasa del borde de la pantalla para abajo, no imprime nada
    if(y > VBE_mode_info->height){
        return;
    }
    
	// hago wrap si me paso del borde de la pantalla horizontalmente
	if(x >= VBE_mode_info->width){
		y += (CHAR_HEIGHT + INTERLINE) * (x / VBE_mode_info->width);
        x = x % VBE_mode_info->width;
    }

    // si es el caracter de borrar, muevo el cursor para atrÃ¡s, seteo el flag para despuÃ©s dejarlo quieto y cambio el caracter a espacio
    if(c == 38){
        is_deleting = 1;
        c = 36;

        if(x == 0){
            x = VBE_mode_info->width - CHAR_WIDTH;
            y -= CHAR_HEIGHT + INTERLINE;
        } else {
            x -= CHAR_WIDTH;
        }
    }

	uint8_t *bitmap = retro_font[c];
	for (uint64_t i = 0; i < CHAR_HEIGHT; i++) {
		for (uint64_t j = 0; j < CHAR_WIDTH; j++) {
			if (bitmap[i] & (1 << (7 - j))) {
				putPixel(textColor, x + j, y + i);
			} else {
                putPixel(bgColor, x + j, y + i);
            }
		}
	}

	x += CHAR_WIDTH * !is_deleting; // si estoy borrando, no incremento x
}


void setCursorLine(uint32_t line){
    y = line * CHAR_HEIGHT + line * INTERLINE;
}

void setCursorColumn(uint32_t column){
    x = column * CHAR_WIDTH;
}

void clearScreen(uint32_t bgColor){
    for(uint32_t i = 0; i < VBE_mode_info->height; i++){
        for(uint32_t j = 0; j < VBE_mode_info->width; j++){
            putPixel(bgColor, j, i);
        }
    }
    x = 0;
    y = 0;
}

